// Helper functions for property data
const getLayouts = () => {
  const layouts = [...new Set(properties.map(property => property.layout))];
  return layouts.sort();
};

const getPropertyTypes = () => {
  const types = [...new Set(properties.map(property => property.propertyType))];
  return types.sort();
};

const filterProperties = (filters) => {
  return properties.filter(property => {
    // Apply filters
    if (filters.title && !property.title.toLowerCase().includes(filters.title.toLowerCase())) return false;
    if (filters.minPrice && property.price < filters.minPrice) return false;
    if (filters.maxPrice && property.price > filters.maxPrice) return false;
    if (filters.address && !property.address.toLowerCase().includes(filters.address.toLowerCase())) return false;
    if (filters.layout && property.layout !== filters.layout) return false;
    if (filters.minArea && property.area < filters.minArea) return false;
    if (filters.maxArea && property.area > filters.maxArea) return false;
    if (filters.propertyType && property.propertyType !== filters.propertyType) return false;
    if (filters.petsAllowed && !['Allowed', 'Negotiable'].includes(property.pets)) return false;
    if (filters.transportation && !property.transportation.toLowerCase().includes(filters.transportation.toLowerCase())) return false;
    if (filters.yearBuilt && !property.yearBuilt.includes(filters.yearBuilt)) return false;
    
    return true;
  });
};

export { properties, getLayouts, getPropertyTypes, filterProperties };
export default properties;